-- Extract task-instance level runtime facts from the Airflow metadata DB.
SELECT
    id,
    dag_id,
    task_id,
    run_id,
    map_index,
    state,
    try_number,
    max_tries,
    queued_dttm,
    scheduled_dttm,
    start_date,
    end_date,
    duration,
    operator,
    custom_operator_name,
    pool,
    queue,
    priority_weight,
    queued_by_job_id,
    trigger_id,
    trigger_timeout,
    next_method,
    next_kwargs,
    executor,
    updated_at,
    span_status
FROM task_instance;
